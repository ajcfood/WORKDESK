            FREELIST GROUPS  1
            BUFFER_POOL      DEFAULT
           );

ALTER TABLE WORKDESK.CM_GROUP_MEMBER ADD (
  CONSTRAINT PK_CM_GROUP_MEMBER
  PRIMARY KEY
  (TK_GROUP, TK_EMPLOYEE)
  USING INDEX WORKDESK.PK_CM_GROUP_MEMBER
  ENABLE VALIDATE);


