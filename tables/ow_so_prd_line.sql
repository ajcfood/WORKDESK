(
  TK_OW            NUMBER(7)                    NOT NULL,
  LINE_NUM         NUMBER(5)                    NOT NULL,
  SELL_PRICE_CASE  NUMBER(15,7),
  SELL_PRICE_WT    NUMBER(15,7),
  SELL_PRICE_UOM   VARCHAR2(6 BYTE)
)
TABLESPACE WORKDESK_DATA
PCTUSED    40
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            INITIAL          1M
            NEXT             40K
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            FREELISTS        1
            FREELIST GROUPS  1
            BUFFER_POOL      DEFAULT
           )
LOGGING 
NOCOMPRESS 
NOCACHE;

