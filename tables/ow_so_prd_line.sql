COMMENT ON COLUMN WORKDESK.OW_SO_PRD_LINE.TK_OW IS 'This is the id for the worksheet.';

COMMENT ON COLUMN WORKDESK.OW_SO_PRD_LINE.LINE_NUM IS 'This is the worksheet line number.';

COMMENT ON COLUMN WORKDESK.OW_SO_PRD_LINE.SELL_PRICE_CASE IS 'This is the price per case.';

COMMENT ON COLUMN WORKDESK.OW_SO_PRD_LINE.SELL_PRICE_WT IS 'This is the price per UOM (other than case)';

COMMENT ON COLUMN WORKDESK.OW_SO_PRD_LINE.SELL_PRICE_UOM IS 'Selling Price UOM';


